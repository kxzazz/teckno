// AND => &
console.log(0 & 0);
console.log(0 & 1);
console.log(1 & 0);
console.log(1 & 1);

// OR => |
console.log(0 | 0);
console.log(0 | 1);
console.log(1 | 0);
console.log(1 | 1);

// NOT => ~
console.log(~0);
console.log(~1);

//XOR => ^
console.log(0 ^ 0);
console.log(0 ^ 1);
console.log(1 ^ 0);
console.log(1 ^ 1);

//Lshift => <<
console.log(1 << 9);

//Rshift => >>
console.log(548 >> 5);

//RShift with zerofill => >>>
console.log(548 >>> 5);
